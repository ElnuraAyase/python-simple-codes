# Step 1: Ask the user to input the length of the rectangle
length = float(input("Enter the length of the rectangle: "))

# Step 2: Ask the user to input the width of the rectangle
width = float(input("Enter the width of the rectangle: "))

# Step 3: Calculate the area of the rectangle
area = length * width

# Step 4: Display the calculated area
print("The area of the rectangle with length", length, "and width", width, "is:", area)
